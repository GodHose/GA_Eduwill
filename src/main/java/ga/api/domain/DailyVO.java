package ga.api.domain;

public class DailyVO {
	
	private String mDate;
	private int mPageView;
	private int mUniquePageviews;
	private int mSessions;
	private int mBounces;
	private double mBounceRate;
	private int mTotalEvents;
	private double mEventRate;
	private int mEntrances;
	
	public String getmDate() {
		return mDate;
	}
	public void setmDate(String mDate) {
		this.mDate = mDate;
	}
	public int getmPageView() {
		return mPageView;
	}
	public void setmPageView(int mPageView) {
		this.mPageView = mPageView;
	}
	public int getmUniquePageviews() {
		return mUniquePageviews;
	}
	public void setmUniquePageviews(int mUniquePageviews) {
		this.mUniquePageviews = mUniquePageviews;
	}
	public int getmSessions() {
		return mSessions;
	}
	public void setmSessions(int mSessions) {
		this.mSessions = mSessions;
	}
	public int getmBounces() {
		return mBounces;
	}
	public void setmBounces(int mBounces) {
		this.mBounces = mBounces;
	}
	public double getmBounceRate() {
		return mBounceRate;
	}
	public void setmBounceRate(double mBounceRate) {
		this.mBounceRate = mBounceRate;
	}
	public int getmTotalEvents() {
		return mTotalEvents;
	}
	public void setmTotalEvents(int mTotalEvents) {
		this.mTotalEvents = mTotalEvents;
	}
	public double getmEventRate() {
		return mEventRate;
	}
	public void setmEventRate(double mEventRate) {
		this.mEventRate = mEventRate;
	}
	public int getmEntrances() {
		return mEntrances;
	}
	public void setmEntrances(int mEntrances) {
		this.mEntrances = mEntrances;
	}
	
	
}
